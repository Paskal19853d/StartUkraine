    if not fields:
        return {"ok": True}
    vals.append(uid)
    db = get_db()
    with db.cursor() as c:
        c.execute(f"UPDATE users SET {', '.join(fields)} WHERE id=%s", vals)
    db.commit()
    db.close()
    return {"ok": True}

