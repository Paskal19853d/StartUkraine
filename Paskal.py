    me = require_admin(request)
    fields, vals = [], []
    if body.name is not None:
        fields.append("name=%s"); vals.append(_sanitize_text(body.name, 100))
    if body.email is not None:
        fields.append("email=%s"); vals.append(body.email.lower().strip()[:120])
