@app.get("/api/search")
def search(q: str = "", request: Request = None):
    ip = _get_ip(request) if request else "unknown"
    # 30 запитів / хв з одного IP
    if not _rl.check(f"search:{ip}", 30, 60):
        raise HTTPException(429, "Забагато пошукових запитів. Зачекайте хвилину.")
    q = q.strip()[:200]
    if len(q) < 2:
        return []
    db = get_db()
    with db.cursor() as c:
        c.execute("SELECT * FROM memorials WHERE approved=1")
        rows = c.fetchall()

    scored = []
    for r in rows:
        score = _score_person(r, q)
        if score > 0.3:
            scored.append((score, r))
    scored.sort(key=lambda x: (-x[0], -(x[1].get("rating") or 0)))

    results = []
    for score, r in scored[:10]:
        results.append({
            "id":       r["id"],
            "name":     f"{r['last']} {r['first']} {r.get('mid','') or ''}".strip(),
            "last":     r["last"],
            "first":    r["first"],
            "mid":      r.get("mid", "") or "",
            "callsign": r.get("grp", "") or "",
            "location": r.get("loc", "") or "",
            "bury":     r.get("bury", "") or "",
            "color":    r.get("color", "#4fc3f7"),
            "x":        r.get("pos_x", 0.5),
            "y":        r.get("pos_y", 0.5),
            "likes":    r.get("likes", 0),
            "score":    round(score, 3),
        })

    # Логуємо запит (обмежуємо до 10000 записів)
    try:
        with db.cursor() as c:
            c.execute(
                "INSERT INTO search_logs (query, results_count, created_at) VALUES (%s,%s,%s)",
                (q, len(results), int(time.time()))
            )
            # Видаляємо старі записи щоб не переповнювати
            c.execute("""
                DELETE FROM search_logs
                WHERE id NOT IN (
                    SELECT id FROM (
                        SELECT id FROM search_logs ORDER BY id DESC LIMIT 10000
                    ) AS t
                )
            """)
        db.commit()
    except Exception:
        pass
    db.close()
    return results

