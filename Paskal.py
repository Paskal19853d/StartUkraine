@app.get("/api/admin/users")
def get_users(request: Request):
    require_admin(request)
    db = get_db()
    with db.cursor() as c:
        c.execute(
            "SELECT id,name,email,is_admin,is_banned,ban_until,notes,last_seen,created FROM users ORDER BY id DESC"
        )
        rows = c.fetchall()
    db.close()
    now = int(time.time())
    for r in rows:
        r["online"] = (now - (r["last_seen"] or 0)) < 120
        bu = r.get("ban_until") or 0
        if r["is_banned"]:
            if bu > 0:
                remaining = bu - now
                if remaining <= 0:
                    r["ban_status"] = "expired"
                    r["ban_remaining"] = 0
                else:
                    r["ban_status"] = "temp"
                    r["ban_remaining"] = remaining
            else:
                r["ban_status"] = "perm"
                r["ban_remaining"] = 0
        else:
            r["ban_status"] = "active"
            r["ban_remaining"] = 0
    return rows

